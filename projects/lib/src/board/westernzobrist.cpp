quint64 WesternZobrist::piece(const Piece& piece, int square) const
{
    Q_ASSERT(piece.isValid());
    Q_ASSERT(piece.type() >= 0 && piece.type() < pieceTypeCount());
    Q_ASSERT(square >= 0 && square < squareCount());

    int arwidth = 9 + 2;
    int file = (square % arwidth) - 1;
    int rank = (10 - 1) - ((square / arwidth) - 2);
    int s90 = (9 - rank) * 9 + file;
    int pt = piece.type();
    int side = piece.side();
    int p15 = pt + (side * 7);

    Q_ASSERT(p15 < 15);

    return ZobristTable[T15[p15]][C90[s90]];
}

